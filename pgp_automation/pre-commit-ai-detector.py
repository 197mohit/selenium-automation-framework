def get_git_info():
    """Get current Git repository information"""
    try:
        repo_path = os.popen('git rev-parse --show-toplevel').read().strip()
        current_branch = os.popen('git branch --show-current').read().strip()
        commit_hash = os.popen('git rev-parse HEAD').read().strip()
        return repo_path, current_branch, commit_hash
    except Exception as e:
        print(f"❌ Error getting Git info: {e}")
        return None, None, None

def get_staged_files():
    """Get list of staged files that will be committed"""
    try:
        staged_files = os.popen('git diff --cached --name-only').read().strip()
        if not staged_files:
            return []
        return [f.strip() for f in staged_files.split('\n') if f.strip()]
    except Exception as e:
        print(f"❌ Error getting staged files: {e}")
        return []

def detect_ai_patterns(file_path):
    """Detect AI-generated code patterns in a file"""
    try:
        with open(file_path, 'r', encoding='utf-8', errors='ignore') as f:
            content = f.read()
        
        results = {
            'high_confidence': [],
            'medium_confidence': [],
            'low_confidence': [],
            'total_matches': 0
        }
        
        for confidence_level, patterns in AI_PATTERNS.items():
            for pattern in patterns:
                matches = re.findall(pattern, content, re.IGNORECASE)
                if matches:
                    results[confidence_level].append({
                        'pattern': pattern,
                        'count': len(matches),
                        'matches': matches[:3]  # Limit to first 3 matches
                    })
                    results['total_matches'] += len(matches)
        
        return results
    except Exception as e:
        print(f"⚠️ Warning: Could not read {file_path}: {e}")
        return None

def calculate_ai_score(detection_results):
    """Calculate AI generation likelihood score (0-100)"""
    if not detection_results:
        return 0
    
    score = 0
    # Fix: Count the actual pattern matches, not the length of the results
    score += len(detection_results['high_confidence']) * 10
    score += len(detection_results['medium_confidence']) * 5
    score += len(detection_results['low_confidence']) * 2
    
    # Cap at 100
    return min(score, 100)

def analyze_staged_files():
    """Analyze all staged files for AI-generated code"""
    staged_files = get_staged_files()
    if not staged_files:
        print("ℹ️ No staged files to analyze")
        return None, None
    
    ai_generated_files = []
    total_files = 0
    file_analysis = {}
    
    for file_path in staged_files:
        # Only analyze code files
        if not any(file_path.endswith(ext) for ext in CODE_EXTENSIONS):
            continue
        
        if not os.path.exists(file_path):
            continue
        
        total_files += 1
        detection_results = detect_ai_patterns(file_path)
        
        if detection_results and detection_results['total_matches'] > 0:
            ai_score = calculate_ai_score(detection_results)
            file_analysis[file_path] = {
                'ai_score': ai_score,
                'detection_results': detection_results,
                'is_ai_generated': ai_score >= 30  # Threshold for AI generation
            }
            
            if ai_score >= 30:
                ai_generated_files.append(file_path)
    
    return file_analysis, total_files

def log_ai_usage(file_analysis, total_files):
    """Log AI usage statistics"""
    timestamp = datetime.datetime.now().isoformat()
    repo_path, current_branch, commit_hash = get_git_info()
    
    # Calculate statistics
    ai_files = [f for f, analysis in file_analysis.items() if analysis['is_ai_generated']]
    ai_percentage = (len(ai_files) / total_files * 100) if total_files > 0 else 0
    
    # Log to tracking file
    log_entry = {
        'timestamp': timestamp,
        'commit_hash': commit_hash,
        'branch': current_branch,
        'total_files': total_files,
        'ai_files': len(ai_files),
        'ai_percentage': round(ai_percentage, 2),
        'files': file_analysis
    }
    
    # Append to tracking log
    with open(AI_TRACKING_FILE, 'a') as f:
        f.write(json.dumps(log_entry) + '\n')
    
    # Update stats file
    update_stats_file(log_entry)
    
    return ai_percentage, len(ai_files)

def update_stats_file(log_entry):
    """Update cumulative statistics file"""
    stats_file = AI_STATS_FILE
    stats = {}
    
    if os.path.exists(stats_file):
        try:
            with open(stats_file, 'r') as f:
                stats = json.load(f)
        except:
            stats = {}
    
    # Initialize if needed
    if 'commits' not in stats:
        stats['commits'] = []
    if 'summary' not in stats:
        stats['summary'] = {
            'total_commits': 0,
            'total_files': 0,
            'total_ai_files': 0,
            'average_ai_percentage': 0
        }
    
    # Add current commit
    stats['commits'].append(log_entry)
    stats['summary']['total_commits'] += 1
    stats['summary']['total_files'] += log_entry['total_files']
    stats['summary']['total_ai_files'] += log_entry['ai_files']
    
    # Calculate average
    if stats['summary']['total_commits'] > 0 and stats['summary']['total_files'] > 0:
        stats['summary']['average_ai_percentage'] = round(
            (stats['summary']['total_ai_files'] / stats['summary']['total_files']) * 100, 2
        )
    
    # Keep only last 100 commits to avoid huge files
    if len(stats['commits']) > 100:
        stats['commits'] = stats['commits'][-100:]
    
    # Save updated stats
    with open(stats_file, 'w') as f:
        json.dump(stats, f, indent=2)

