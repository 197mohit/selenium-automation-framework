# Configuration
AI_TRACKING_FILE = '.ai_tracking.log'
AI_STATS_FILE = '.ai_stats.json'
REQUIRED_AI_MARKER = False  # Set to True if you want to require AI markers

# File extensions to scan
CODE_EXTENSIONS = {
    '.py', '.js', '.ts', '.jsx', '.tsx', '.java', '.cpp', '.c', '.h', 
    '.hpp', '.cs', '.php', '.rb', '.go', '.rs', '.swift', '.kt', '.scala',
    '.html', '.css', '.scss', '.sass', '.less', '.vue', '.svelte'
}

